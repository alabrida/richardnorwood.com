'use client';

import Link from 'next/link';
import { usePathname, useRouter } from 'next/navigation';
import { createClient } from '@/lib/supabase/client';
import { toast } from 'sonner';

const navigation = [
    { name: 'Dashboard', href: '/dashboard', icon: HomeIcon },
    { name: 'Revenue Reports', href: '/dashboard/reports', icon: ChartIcon },
    { name: 'Strategy Chat', href: '/dashboard/pro-tools/chat', icon: SparklesIcon },
    { name: 'Page Generator', href: '/dashboard/pro-tools/generator', icon: LightningIcon },
    { name: 'Settings', href: '/dashboard/settings', icon: SettingsIcon },
];

export function Sidebar() {
    const pathname = usePathname();
    const router = useRouter();
    const supabase = createClient();

    async function handleLogout() {
        await supabase.auth.signOut();
        toast.success('Logged out successfully');
        router.push('/login');
        router.refresh();
    }

    return (
        <div className="flex flex-col w-64 bg-[var(--color-card)] border-r border-[var(--color-border)] h-screen sticky top-0">
            {/* Logo */}
            <div className="p-6 border-b border-[var(--color-border)]">
                <Link href="/" className="text-xl font-bold text-gradient">
                    Richard Norwood
                </Link>
            </div>

            {/* Navigation */}
            <nav className="flex-1 p-4 space-y-2">
                {navigation.map((item) => {
                    const isActive = pathname === item.href;
                    return (
                        <Link
                            key={item.name}
                            href={item.href}
                            className={`flex items-center gap-3 px-4 py-3 rounded-[var(--radius-lg)] transition-colors ${isActive
                                ? 'bg-[var(--color-primary)]/10 text-[var(--color-primary)] font-medium'
                                : 'text-[var(--color-muted)] hover:bg-[var(--color-background)] hover:text-[var(--color-foreground)]'
                                }`}
                        >
                            <item.icon className="w-5 h-5" />
                            {item.name}
                        </Link>
                    );
                })}
            </nav>

            {/* User / Logout */}
            <div className="p-4 border-t border-[var(--color-border)]">
                <button
                    onClick={handleLogout}
                    className="flex items-center gap-3 w-full px-4 py-3 text-[var(--color-muted)] hover:text-[var(--color-error)] hover:bg-[var(--color-error)]/10 rounded-[var(--radius-lg)] transition-colors"
                >
                    <LogoutIcon className="w-5 h-5" />
                    Sign Out
                </button>
            </div>
        </div>
    );
}

